// class name
public class Reverse_Star_pattern {
	public static void main(String[] args) {
		
		for(int i = 5; i >= 1; i--){    // value of outer loop
			for(int j = 1; j <= i; j++){   // value of inner loop
				System.out.print("*");  // print star
					}
				System.out.println();
		           } 
	           }
            }
          
		     
